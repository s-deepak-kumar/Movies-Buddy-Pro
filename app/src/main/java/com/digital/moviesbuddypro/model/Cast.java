package com.digital.moviesbuddypro.model;

public class Cast {
    private String mName, mImage;

    public Cast(){}

    public String getName() {
        return mName;
    }

    public void setName(String mName) {
        this.mName = mName;
    }

    public String getImage() {
        return mImage;
    }

    public void setImage(String mImage) {
        this.mImage = mImage;
    }
}
