package com.digital.moviesbuddypro.model;

public class Episode {
    String mSeasonPoster, mEpisodeTitle, mEpisodeYouTubeVideoID;

    public String getSeasonPoster() {
        return mSeasonPoster;
    }

    public void setSeasonPoster(String mSeasonPoster) {
        this.mSeasonPoster = mSeasonPoster;
    }

    public String getEpisodeTitle() {
        return mEpisodeTitle;
    }

    public void setEpisodeTitle(String mEpisodeTitle) {
        this.mEpisodeTitle = mEpisodeTitle;
    }

    public String getEpisodeYouTubeVideoID() {
        return mEpisodeYouTubeVideoID;
    }

    public void setEpisodeYouTubeVideoID(String mEpisodeYouTubeVideoID) {
        this.mEpisodeYouTubeVideoID = mEpisodeYouTubeVideoID;
    }
}
