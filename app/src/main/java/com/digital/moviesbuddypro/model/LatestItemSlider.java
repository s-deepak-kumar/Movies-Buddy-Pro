package com.digital.moviesbuddypro.model;

public class LatestItemSlider {

    private String mId, mItemTitle, mItemCategory, mItemPoster, mItemCertificate, mItemQualityType, mItemIMDBRating, mItemSeasonTag;

    public LatestItemSlider(){}

    public String getId() {
        return mId;
    }

    public void setId(String mId) {
        this.mId = mId;
    }

    public String getItemTitle() {
        return mItemTitle;
    }

    public void setItemTitle(String mItemTitle) {
        this.mItemTitle = mItemTitle;
    }

    public String getItemCategory() {
        return mItemCategory;
    }

    public void setItemCategory(String mItemCategory) {
        this.mItemCategory = mItemCategory;
    }

    public String getItemPoster() {
        return mItemPoster;
    }

    public void setItemPoster(String mItemPoster) {
        this.mItemPoster = mItemPoster;
    }

    public String getItemCertificate() {
        return mItemCertificate;
    }

    public void setItemCertificate(String mItemCertificate) {
        this.mItemCertificate = mItemCertificate;
    }

    public String getItemQualityType() {
        return mItemQualityType;
    }

    public void setItemQualityType(String mItemQualityType) {
        this.mItemQualityType = mItemQualityType;
    }

    public String getItemIMDBRating() {
        return mItemIMDBRating;
    }

    public void setItemIMDBRating(String mItemIMDBRating) {
        this.mItemIMDBRating = mItemIMDBRating;
    }

    public String getItemSeasonTag() {
        return mItemSeasonTag;
    }

    public void setItemSeasonTag(String mItemSeasonTag) {
        this.mItemSeasonTag = mItemSeasonTag;
    }
}
